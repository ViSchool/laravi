     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        	$this->validate(request(), [
		'email' => 'required|email',
        ]);
		$inquiry = new Inquiry;
		$inquiry->lehrername = $request->lehrername;
		$inquiry->fach = $request->fach;
		$inquiry->thema = $request->thema;
		$inquiry->email = $request->email;
		$inquiry->save();
		return redirect()->route('inquiries.index')->with('message', 'Wir haben Deine Anfrage erhalten und melden uns schnellstm√∂glich bei Dir!');
    }

