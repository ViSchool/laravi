    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        {
		$tag = Tag::findOrFail($id);
		$tag->posts()->detach();
		$tag->contents()->detach();
		$tag->series()->detach();
		$tag->delete();
        return redirect('backend/tags');
    }
